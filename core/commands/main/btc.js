const { MessageEmbed } = require("discord.js");
const fetch = require('node-fetch');

exports.launch = (client, message, args, lang) => {
    args[0] = args[0].toUpperCase();

    if (!args[0] || !currencyCodes.find(code => args[0] === code)) return;
    
    const embed = new MessageEmbed()
        .setColor(0x2F3136)
        .setTitle("Bitcoin Price")
        .setTimestamp()
        .setFooter("gokium", client.user.displayAvatarURL({format: "png" || "gif"}));
        
    fetch(`https://api.nomics.com/v1/currencies/ticker?key=0d0441f5c761182e7d49a5fc5a0dacdb&ids=BTC&interval=1h&convert=${args[0]}&per-page=100&page=1`)
    .then(response => response.json())
    .then(body => {
        if (!body) return;
        
        const price = parseFloat(body[0].price);
        embed.addField(`**${args[0]}**`, price.toFixed(2));
        message.channel.send(embed);
    })
}